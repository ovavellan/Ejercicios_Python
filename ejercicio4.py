# Ingresar la base y la altura de un triángulo y hallar su área.

#Ingresar la base de un triángulo 
base = float(input("Ingrese la base del triángulo cuya área desea calcular: "))

#Ingresar la altura de un triángulo
altura = float(input("Ingrese la altura del triángulo cuya altura desea calcular: "))

#Calculos para hallar el área de un triángulo 
area = (base * altura)/2

#Imprimir resultado del área de un triángulo
print("El área del triángulo es:",area) 

